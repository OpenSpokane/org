<h4>About the WorkSource Spokane Connection site</h4>
Our WorkSource Spokane Connection site offers computers for individuals who need them for employment-related activities. If you’re working on your resume or applying for jobs online, this is a good location to do that work. 

<h4>Using the WorkSource computer lab</h4>
Unlike the library’s other Internet computers, use of these computers has <b>no time limit</b>.
<ul>
    <li>Reserved for employment-related work</li>
    <li>No library card required</li>
    <li>No time limit on computers</li>
    <li>No charge for printing</li>
    <li>Individual assistance provided</li>
    <li>First come, first served</li>
</ul>


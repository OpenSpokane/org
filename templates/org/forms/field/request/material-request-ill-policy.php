<p>
  Interlibrary Loan is the process by which Spokane Public Library requests materials from, or supplies materials to, libraries outside of our library system.  
</p>

<h4>Who Can Borrow?</h4>
<p>
Library card holders who reside within the city limits of Spokane and people who own businesses within the city of Spokane, if they have a business library card. In order to process a request <b>ALL</b> of the following must apply:
</p>
<ul>
  <li>Card must be <b>verified</b> (proof of address and identity must be shown).</li>
  <li>Fines must be under <b>$10.00</b>.</li>
  <li>Card must be <b>current</b> (no expired cards, please).</li>
</ul>
<p>
  Non-resident cardholders should contact their home library system for Interlibrary Loan Service.
</p>

<h4>What Can I Borrow?</h4>
<p>
  Customers may only request the same title <b>twice</b> per calendar year. 
</p>
<ul>
  <li>Books (including Audiobooks on cassette or CD, but NOT downloadable) published prior to the last 6 months.</li>
  <li>Photocopies of journal/magazine articles or genealogical information.</li>
  <li>Microfilm or fiche of newspapers.</li>
  <li>Theses or dissertations.</li>
</ul>
<p>
  The owning library will make the decision as to whether or not they will lend the material.
</p>

<h4>What Am I Unable to Request?</h4>
<ul>
  <li>Videos, DVDs, music cassettes and CDs, downloadable Audiobooks, downloadable eBooks and CD-Roms, genealogy books (though we can request photocopies from genealogy items).</li>
  <li>Books already owned by Spokane Public Library (including items located within our Reference, Northwest Room, and Genealogy collections).</li>
</ul>

<h4>How Many Items Can I Request?</h4>
<ul>
  <li>Only <b>four items</b> can be in process at a time. This includes items that could only be used in the library and microfilm.  For each item that has been returned another item can be requested.</li>
  <li>Microfilm requests must be for a reasonable date range in order to be processed.</li>
  <li>Requests must be submitted on Spokane Public Library forms. No photocopies of the form will be accepted.</li>
</ul>

<h4>How Long Will It Take?</h4>
<p>
  Interlibrary Loan items are borrowed from across the United States and Canada. 
</p>
<p>
  It can take <b>4&ndash;6 weeks</b> to receive an item.
</p>

<h4>How Do I Find Out When the Item Arrives?</h4>
<ul>
  <li>When the item arrives a hold will be placed on the item and a <b>hold notice</b> (mail, e-mail or telephone) <b>will be generated as soon as the item reaches the branch</b> specified as the pick-up location.</li>
  <li>Since only 20 items can be "On Hold" at any given time, please reserve space on your hold list for interlibrary loan items.</li>
  <li>Interlibrary loan materials will be held <b>behind the circulation desk</b>, not on the regular hold shelf. </li>
  <li>You will have <b>five days</b> to pick up your items before they will be sent back to the lending library.</li>
</ul>

<h4>How Long Can I Keep an Interlibrary Loan?</h4>
<p>
  Interlibrary loan materials will check out for a <b>maximum of three weeks</b> from the day you pick up the book. 
</p>
<p>
  Because Spokane Public Library does not own items borrowed using the Interlibrary Loan system,  <b>renewals are not allowed</b>. 
</p>

<h4>Will I Find Out If You Cannot Locate an Item?</h4>
<p>
  The Interlibrary Loan department will let you know <b>by placing a block on the library card used to request the item</b> if we are unable to borrow the item you requested.
</p>
